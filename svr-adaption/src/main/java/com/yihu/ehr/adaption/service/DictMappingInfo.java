public class DictMappingInfo {

    String id;
    String stdDictId;
    String orgDictId;
    String stdDictCode;
    String orgDictCode;
    String orgDictName;
    String stdDictName;
    String planId;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getStdDictId() {
        return stdDictId;
    }

    public void setStdDictId(String stdDictId) {
        this.stdDictId = stdDictId;
    }

    public String getOrgDictId() {
        return orgDictId;
    }

    public void setOrgDictId(String orgDictId) {
        this.orgDictId = orgDictId;
    }

    public String getStdDictCode() {
        return stdDictCode;
    }

    public void setStdDictCode(String stdDictCode) {
        this.stdDictCode = stdDictCode;
    }

    public String getOrgDictCode() {
        return orgDictCode;
    }

    public void setOrgDictCode(String orgDictCode) {
        this.orgDictCode = orgDictCode;
    }

    public String getPlanId() {
        return planId;
    }

    public void setPlanId(String planId) {
        this.planId = planId;
    }

    public String getOrgDictName() {
        return orgDictName;
    }

    public void setOrgDictName(String orgDictName) {
        this.orgDictName = orgDictName;
    }

    public String getStdDictName() {
        return stdDictName;
    }

    public void setStdDictName(String stdDictName) {
        this.stdDictName = stdDictName;
    }
}
